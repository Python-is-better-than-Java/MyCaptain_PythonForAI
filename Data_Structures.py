# Operations on set - Union, Intersection, Difference, Symmetric Difference
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

print("A: ", A)
print("B: ", B)
print("Union of the two sets: ", A.union(B))
print("Intersection of the two sets: ", A.intersection(B))
print("Difference between the two sets: ", A.difference(B))
print("Symmetric difference between the two sets: ", A.symmetric_difference(B))
